    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Account acc = new Account();
        AccountDao adao = new AccountDao();
        decript d = new decript();
        if(pass.getText().equals(cpass.getText())){
            acc.setFirstname(fname.getText());
            acc.setLastname(lname.getText());
            acc.setUsername(uname.getText());
            acc.setPassword(d.econde(pass.getText()));
            acc.setEmail(email.getText());
            adao.createAccount(acc);
            JOptionPane.showMessageDialog(this, "Account Created !!!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

