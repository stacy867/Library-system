    private void formInternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameOpened
        Object[] colnames = {"OPERATION NUM.","CLIENT NAME","BOOK NAME","TAKEN/RETURNED DATE","OPERATION CAT.","STATUS"};
        DefaultTableModel tm = (DefaultTableModel) jTable1.getModel();
        tm.setRowCount(0);
        tm.setColumnIdentifiers(colnames);
        CheckinoutDao ckdao =  new CheckinoutDao();
        ClientDao cdao = new ClientDao();
        BookDao bdao = new BookDao();
        List<Checkinout> operations = ckdao.getCheckInOut();
        for(Checkinout c:operations){
            Object obj[] ={c.getOpnumber(),cdao.getClientName(c.getClient().getRegno()),bdao.getName(c.getBook().getBookid()),c.getDatetime(),c.getOperationcategory(),c.getStatus()};
            tm.addRow(obj);
        }
        jTable1.setModel(tm);
    }//GEN-LAST:event_formInternalFrameOpened

